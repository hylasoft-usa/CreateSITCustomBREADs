
namespace SIT.Libs.Base.CAB.Web.Controls.Popup.Enumerations
{
    /// <summary>
    /// An enumeration listing all the possible position where popup can appear when made visible
    /// </summary>
    public enum PopupPosition
    {
        /// <summary>
        /// Popup will appear in the top left corner of the viewport
        /// </summary>
        TopLeft,

        /// <summary>
        /// Popup will appear in the left area of the viewport
        /// </summary>
        Left,

        /// <summary>
        /// Popup will appear in the bottom left corner of the viewport
        /// </summary>
        BottomLeft,

        /// <summary>
        /// Popup will appear in the central upper area of the viewport
        /// </summary>
        TopCenter,

        /// <summary>
        /// Popup will appear centered in the viewport
        /// </summary>
        Center,

        /// <summary>
        /// Popup will appear in the central lower area of the viewport
        /// </summary>
        BottomCenter,

        /// <summary>
        /// Popup will appear in the top right corner
        /// </summary>
        TopRight,

        /// <summary>
        /// Popup will appear in the right area of the viewport
        /// </summary>
        Right,

        /// <summary>
        /// Popup will appear in the bottom right corner of the viewport
        /// </summary>
        BottomRight
    }
}
